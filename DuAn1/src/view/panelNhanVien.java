    private void btnAddNVActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddNVActionPerformed
        String ma = txtMa.getText().trim();
        if (service.checkMa(ma) > 0) {
            JOptionPane.showMessageDialog(this, "ma " + ma + " da ton tai");
            return;
        }
        if (service.add(getForm()) > 0) {
            JOptionPane.showMessageDialog(this, "Thêm thành công");

            this.fillTable(service.getAll());
            this.resetForm();
        } else {
            JOptionPane.showMessageDialog(this, "Thêm lỗi");
        }

    }//GEN-LAST:event_btnAddNVActionPerformed

