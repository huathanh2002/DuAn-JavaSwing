    private void btnUpdateNVActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateNVActionPerformed
        index = tblList.getSelectedRow();
        if (index == -1) {
            JOptionPane.showMessageDialog(this, "Bạn chưa chọn dòng cần sửa !");
        } else {
            if (txtMa.getText().equals(tblList.getValueAt(index, 0))) {
                if (service.update(getForm(), service.idNV(txtMa.getText())) > 0) {
                    JOptionPane.showMessageDialog(this, "sửa thành công");
                    this.fillTable(service.getAll());
                    this.resetForm();
                } else {
                    JOptionPane.showMessageDialog(this, "sửa thất bại");
                    this.resetForm();
                }
            } else {
                String ma = txtMa.getText().trim();
                if (service.checkMa(ma) > 0) {
                    JOptionPane.showMessageDialog(this, "ma " + ma + " da duoc dung cho nhan vien khac");
                    return;
                } else {
                    if (service.update(getForm(), service.idNV(txtMa.getText())) > 0) {
                        JOptionPane.showMessageDialog(this, "sửa thành công");
                        this.fillTable(service.getAll());
                        this.resetForm();
                    } else {
                        JOptionPane.showMessageDialog(this, "sửa thất bại");
                        this.resetForm();
                    }
                }
            }

        }
        
        
    }//GEN-LAST:event_btnUpdateNVActionPerformed

