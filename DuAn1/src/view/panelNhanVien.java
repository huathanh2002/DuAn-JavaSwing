    void setModel(NhanVienModel nv) {
        txtMa.setText(nv.getMa());
        txtTen.setText(nv.getHoTen());

        txtMatKhau.setText(nv.getMatKhau());
        txtNgaySinh.setDate(nv.getNgaySinh());
        txtDiaChi.setText(nv.getDiaChi());
        txtSdtNV.setText(nv.getsDT());

        if (nv.getGioiTinh().equalsIgnoreCase("1")) {
            rdoNam.setSelected(true);
        } else {
            rdoNu.setSelected(true);
        }
        if (nv.getChucVu().equalsIgnoreCase("Nhân viên")) {
            cboChucVu.setSelectedItem("Nhân viên");
        } else if (nv.getChucVu().equalsIgnoreCase("Quản lý")) {
            cboChucVu.setSelectedItem("Quản lý");
        }
    }

    NhanVienModel getForm() {
        String ma, hoTen, matKhau, sDT, gioiTinh, chucVu;
        Date ngaySinh;
        String diaChi,trangThai="Hoạt động";

        ma = txtMa.getText().trim();
        if (txtMa.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "bạn chưa nhập mã ");
            return null;
        }
        if (cboChucVu.getSelectedItem().equals("Nhân viên")) {
            chucVu = "Nhân viên";
        } else {
            chucVu = "Quản lý";
        }
        hoTen = txtTen.getText().trim();
        if (hoTen.isEmpty()) {
            JOptionPane.showMessageDialog(this, "bạn chưa nhập họ tên ");
            return null;
        }
        matKhau = txtMatKhau.getText();
        if (matKhau.isEmpty()) {
            JOptionPane.showMessageDialog(this, "bạn chưa nhập mật khẩu ");
            return null;
        }
        sDT = txtSdtNV.getText().trim();
        if (sDT.isEmpty()) {
            JOptionPane.showMessageDialog(this, "bạn chưa nhập số điện thoại ");
            return null;
        }
        if (rdoNam.isSelected()) {
            gioiTinh = "1";
        } else {
            gioiTinh = "0";
        }

        ngaySinh = txtNgaySinh.getDate();
        if (ngaySinh== null) {
            JOptionPane.showMessageDialog(this, "bạn chưa nhập ngày sinh ");
            return null;
        }
        diaChi = txtDiaChi.getText().trim();
        if (diaChi.isEmpty()) {
            JOptionPane.showMessageDialog(this, "bạn chưa nhập địa chỉ ");
            return null;
        }
        return new NhanVienModel(ma, hoTen, matKhau, sDT, gioiTinh, chucVu, ngaySinh, diaChi,trangThai);
    }

    void fillTable(List<NhanVienModel> list) {
        tblModel = (DefaultTableModel) tblList.getModel();
        tblModel.setRowCount(0);
        for (NhanVienModel nv : list) {
            String sex;
            if(nv.getGioiTinh().equals("1")){
                sex="Nam";
            }else{
                sex="Nữ";
            }
            Object toDataRow[] = {nv.getMa(), nv.getHoTen(), sex, nv.getChucVu(), nv.getMatKhau(), sdf.format(nv.getNgaySinh()), nv.getDiaChi(), nv.getsDT(), nv.getTrangThai()};
            tblModel.addRow(toDataRow);
        }
    }
    
      public void addPlaceHolderStyle(JTextField textField) {
        Font font = textField.getFont();
        font = font.deriveFont(font.ITALIC);
        textField.setFont(font);
        textField.setForeground(Color.gray);

    }

    public void removePlaceHolderStyle(JTextField textField) {
        Font font = textField.getFont();
        font = font.deriveFont(font.PLAIN | Font.BOLD);
        textField.setFont(font);
        textField.setForeground(Color.gray);

    }

